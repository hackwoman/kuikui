function init3DPreview(containerId) {
  const container = document.getElementById(containerId);
  
  // ËøôÈáå‰ºöÂÆûÈôÖÈõÜÊàê‰∏Ä‰∏™3DÂ∫ìÔºà‰æãÂ¶ÇThree.jsÔºâÔºå‰ΩÜ‰∏∫‰∫ÜÂéüÂûãÊºîÁ§∫ÔºåÊàë‰ª¨‰ΩøÁî®ÁÆÄÂçïÁöÑÊóãËΩ¨ÂõæÁâáÊ®°Êãü
  let isDragging = false;
  let startX = 0;
  let currentRotation = 0;
  
  // Ëé∑Âèñ‰∫ßÂìÅÂõæÁâáÂÖÉÁ¥†
  const productImg = container.querySelector('img');
  
  container.addEventListener('mousedown', (e) => {
    isDragging = true;
    startX = e.clientX;
    container.style.cursor = 'grabbing';
  });
  
  window.addEventListener('mouseup', () => {
    isDragging = false;
    container.style.cursor = 'grab';
  });
  
  window.addEventListener('mousemove', (e) => {
    if (!isDragging) return;
    
    const deltaX = e.clientX - startX;
    startX = e.clientX;
    
    currentRotation += deltaX * 0.5;
    productImg.style.transform = `rotateY(${currentRotation}deg)`;
  });
  
  // Ëß¶Êë∏ËÆæÂ§áÊîØÊåÅ
  container.addEventListener('touchstart', (e) => {
    isDragging = true;
    startX = e.touches[0].clientX;
  });
  
  window.addEventListener('touchend', () => {
    isDragging = false;
  });
  
  window.addEventListener('touchmove', (e) => {
    if (!isDragging) return;
    
    const deltaX = e.touches[0].clientX - startX;
    startX = e.touches[0].clientX;
    
    currentRotation += deltaX * 0.5;
    productImg.style.transform = `rotateY(${currentRotation}deg)`;
  });
}

